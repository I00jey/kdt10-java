package _05_Class._02_access_modifier;

// getter, setter 단축키 alt + Insert
public class getterSetter {
    int width;
    int height;

    public int getWidth() {
        return width;
    }

    public void setWidth(int width) {
        this.width = width;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }
}
